-module(genlen).
-export([test/0]).
-export([some/0]).
-vsn(-0).

some()->
	ok.

test()->
	<<42:6>> = <<42:6>>, % 42 dec
	<<10:5>> = <<42:5>>, % 10 dec

	<<X1:6/integer>> = <<42:6>>,
	X1 = 42,

	<<X2:5/integer>> = <<42:5>>,
	X2 = 10,

	<<X:4,Y:2>> = <<42:6>>,
	X = 10,
	Y = 2,

	%<<C1:4,D1:4>> = <<1998:6>> % eror 4+4 != 6
	<<C1:4,D1:2>> = <<1998:6>>, % 11111 (0011 10)
	C1 = 3,
	D1 = 2,

	%<<C2:4,D2:2>> = <<1998:8>> % error 4+2 != 8
	<<C2:6,D2:2>> = <<1998:8>>, % 111 (110011 10)
	C2 = 51,
	D2 = 2,

	ok.

% c(genlen).
%
% genlen:test().

